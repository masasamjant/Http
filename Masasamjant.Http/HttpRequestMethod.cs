namespace Masasamjant.Http
{
    /// <summary>
    /// Defines methods of HTTP request.
    /// </summary>
    public enum HttpRequestMethod : int
    {
        /// <summary>
        /// GET request.
        /// </summary>
        Get = 0,

        /// <summary>
        /// POST request.
        /// </summary>
        Post = 1,

        /// <summary>
        /// PUT request.
        /// </summary>
        Put = 2,

        /// <summary>
        /// DELETE request.
        /// </summary>
        Delete = 3
    }
}
